-- Normalize game results: move from 6 embedded column groups on scoreboard
-- to a dedicated game_result table with SINGLE_TABLE inheritance.
-- Old columns are retained on scoreboard as a data backup.

CREATE TABLE game_result (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_type VARCHAR(31) NOT NULL,
    scoreboard_id BIGINT,
    raw_content VARCHAR(255),
    discord_author VARCHAR(255),
    comment VARCHAR(255),
    puzzle_number INTEGER,
    attempts INTEGER,
    completed BOOLEAN,
    hard_mode BOOLEAN,
    mistakes INTEGER,
    solve_order VARCHAR(255),
    hints_used INTEGER,
    time_string VARCHAR(255),
    total_seconds INTEGER,
    crossword_date DATE,
    duo BOOLEAN,
    lookups INTEGER,
    check_used BOOLEAN,
    CONSTRAINT fk_game_result_scoreboard FOREIGN KEY (scoreboard_id) REFERENCES scoreboard (id)
);

-- Migrate Wordle results
INSERT INTO game_result (game_type, scoreboard_id, raw_content, discord_author, comment,
                         puzzle_number, attempts, completed, hard_mode)
SELECT 'WORDLE', id, wordle_raw_content, wordle_discord_author, wordle_comment,
       wordle_puzzle_number, wordle_attempts, wordle_completed, wordle_hard_mode
FROM scoreboard WHERE wordle_raw_content IS NOT NULL;

